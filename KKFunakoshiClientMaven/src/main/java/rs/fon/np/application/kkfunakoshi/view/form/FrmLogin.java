    /**
     * Vrsi validaciju korisnickog unosa za korisnicko ime i lozinku.
     * Koristi Validator klasu za proveru i generisanje odgovarajuce poruke o gresci.
     * 
     * @throws ValidationException Ukoliko postoji neka gre≈°ka u unosu
     */
    private void validateForm(String username, String password) throws Exception {
        String errorMessage="";
        if(username.isEmpty()){
            errorMessage+="Username can not be empty!\n";
        }
        if(password.isEmpty()){
            errorMessage+="Password can not be empty!\n";
        }
        if(!errorMessage.isEmpty()){
            throw new Exception("You must fill username and password");
        }
    }
